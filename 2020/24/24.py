class Tile:
    def __init__(self):
        self.neighbours = {}
        self.is_white = True
        self.num = 0

    def __str__(self):
        return ('W' if self.is_white else 'B') + str(self.num)


def print_tile_world(world):
    for num, i in enumerate(world):
        if num % 2 == 1:
            print(' ' + ' '.join([str(j) for j in i]))
        else:
            print(' '.join([str(j) for j in i]))


def create_fully_tiled(h, w):
    all_tiles = []
    c = 0

    for i in range(h):
        cur_line = []

        for j in range(w):
            cur = Tile()
            cur.num = c
            c += 1

            if cur_line != []:
                last = cur_line[-1]
                last.neighbours['e'] = cur
                cur.neighbours['w'] = last

            if all_tiles != []:
                last_line = all_tiles[-1]

                if i % 2 == 1:
                    last = last_line[j]
                    last.neighbours['se'] = cur
                    cur.neighbours['nw'] = last

                    try:
                        last2 = last_line[j+1]
                        last2.neighbours['sw'] = cur
                        cur.neighbours['ne'] = last2
                    except:
                        pass

                else:
                    if j-1 >= 0:
                        last = last_line[j-1]
                        last.neighbours['se'] = cur
                        cur.neighbours['nw'] = last

                    last2 = last_line[j]
                    last2.neighbours['sw'] = cur
                    cur.neighbours['ne'] = last2

            cur_line.append(cur)

        all_tiles.append(cur_line)

    return all_tiles[h//2][w//2], all_tiles

